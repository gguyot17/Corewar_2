#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t	n;

	n = 0;
	while (src[n])
	{
		dest[n] = src[n];
		n++;
	}
	dest[n] = '\0';
	return (dest);
}
